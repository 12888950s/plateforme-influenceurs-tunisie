    "def convertir_stat(valeur_str):\n",
    "    \"\"\"\n",
    "    ✅ BUG 3 CORRIGÉ : conversion immédiate en entier\n",
    "    \"16.7M +\" → 16700000\n",
    "    \"819.7K +\"→ 819700\n",
    "    \"3.3K\"    → 3300\n",
    "    \"893\"     → 893\n",
    "    \"\"        → 0\n",
    "    \"\"\"\n",
    "    if not valeur_str:\n",
    "        return 0\n",
    "    s = str(valeur_str).upper().replace(\" \", \"\").replace(\"+\", \"\").strip()\n",
    "    try:\n",
    "        if \"M\" in s:\n",
    "            return int(float(s.replace(\"M\", \"\")) * 1_000_000)\n",
    "        elif \"K\" in s:\n",
    "            return int(float(s.replace(\"K\", \"\")) * 1_000)\n",
    "        else:\n",
    "            return int(float(s.replace(\",\", \".\")))\n",
    "    except (ValueError, AttributeError):\n",
    "        return 0\n",
