    "    score = 0\n",
    "    total = ig_f + tk_f\n",
    "\n",
    "    if total > 10_000_000:  score += 40\n",
    "    elif total > 5_000_000: score += 35\n",
    "    elif total > 1_000_000: score += 28\n",
    "    elif total > 500_000:   score += 22\n",
    "    elif total > 100_000:   score += 15\n",
    "    elif total > 10_000:    score += 8\n",
    "\n",
    "    if ig_f > 0 and tk_f > 0:  score += 20\n",
    "    elif ig_f > 0 or tk_f > 0: score += 10\n",
    "\n",
    "    if tk_f > 0 and tk_likes > 0:\n",
    "        ratio = tk_likes / tk_f\n",
    "        if ratio > 20:   score += 25\n",
    "        elif ratio > 10: score += 20\n",
    "        elif ratio > 5:  score += 14\n",
    "        elif ratio > 0:  score += 8\n",
    "\n",
    "    if ig_posts > 500:    score += 15\n",
    "    elif ig_posts > 200:  score += 12\n",
    "    elif ig_posts > 50:   score += 8\n",
    "    elif ig_posts > 0:    score += 4\n",
